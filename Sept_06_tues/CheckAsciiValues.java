class CheckAsciiValues 
{
	public static void main(String[] args) 
	{
      
      char a='A';
	  int b=a;

	  char c='$';
	  int d=c;
      
	  char e='a';
	  int f=e;
      
	  char g='h';
	  int h=g;

	  char i='i';
	  int j=i;
     
	  char k='q';
	  int l=k;
     
     
		System.out.println("Ascii value of "+a+" is "+b);
		System.out.println("Ascii value of "+c+" is "+d);
		System.out.println("Ascii value of "+e+" is "+f );
		System.out.println("Ascii value of "+g+" is "+h);
		System.out.println("Ascii value of "+i+" is "+j);
		System.out.println("Ascii value of "+k+" is "+l);
		
	}
}
